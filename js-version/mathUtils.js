// A pure JavaScript function for performing addition on two positive integers                                                                                                                                                     
 function addNumbers(a, b) {
  return a + b;
}


// A Pure JavaScript function for alerting a greeting to a given user
function greetUser(user) {
  return `Hello, ${user.name}! You are ${user.age} years old.`;
}

// Sample usage
console.log(addNumbers(4, 6));
console.log(greetUser({ name: "Amina", age: 25 }));

